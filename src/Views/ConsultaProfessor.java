    public void CarregarTabela() {
        DefaultTableModel tabela = (DefaultTableModel) TabelaProfessor.getModel();
        String codigo;
        String nome;
        String endereco;
        double salario;
        String cpf;
        String departamento;

        for (int i = 0; i < Escola.listaProfessor.size(); i++) {
            codigo = Escola.listaProfessor.get(i).codigo;
            nome = Escola.listaProfessor.get(i).nome;
            endereco = Escola.listaProfessor.get(i).endereco;
            cpf = Escola.listaProfessor.get(i).cpf;
            salario = Escola.listaProfessor.get(i).salario;
            departamento = Escola.listaProfessor.get(i).departamento;
            Object[] dado = {codigo, nome, endereco, cpf, salario, departamento};
            tabela.addRow(dado);
        }
    }

